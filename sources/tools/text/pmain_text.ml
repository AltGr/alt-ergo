open Format
open Options

let () =
  let binary =
    try Sys.argv.(0)
    with _ -> assert false
  in
  fprintf fmt "binary is %s@." binary;
  try
    let distribute = String.equal Sys.argv.(1) "distribute" in
    if not distribute then raise Exit;
    let other_args = Array.sub Sys.argv 2 (Array.length Sys.argv - 2) in
    ()
  with _ ->
    Main_text.main ()
